function down2() {

    var num = parseInt(document.getElementById("num2").value);
    num -= 1;
    if (num <= 0) {
        alert("只能輸入1以上哦!")
    }
    else {
        document.getElementById("num2").value = num;
    }
}

function up2() {
    var num = parseInt(document.getElementById("num2").value);
    num += 1;
    document.getElementById("num2").value = num;

}
